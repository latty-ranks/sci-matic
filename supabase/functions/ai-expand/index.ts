import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { topic, subject, level, mode } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    let systemPrompt = "";
    if (mode === "search") {
      systemPrompt = `You are a knowledgeable science and mathematics tutor for Ugandan secondary students (Senior 1-4). 
Answer questions clearly and simply, aligned with the Uganda UNEB curriculum. 
Use examples relevant to East African students. Keep responses concise but thorough.
Format your response in markdown with headers, bullet points, and bold key terms.
If the question involves formulas, show them clearly.`;
    } else {
      systemPrompt = `You are an expert ${subject} tutor for Ugandan Senior ${level} students following the UNEB curriculum.
The student wants a deeper explanation of the topic: "${topic}".
Provide:
1. A simplified, easy-to-understand explanation with real-world examples from East Africa
2. Additional worked examples with step-by-step solutions
3. Common mistakes students make and how to avoid them
4. Memory aids or mnemonics where helpful
Format your response in markdown with clear headers, bullet points, and bold key terms.
Keep language simple and encouraging.`;
    }

    const userMessage = mode === "search" ? topic : `Please expand on this topic for a Senior ${level} ${subject} student: ${topic}`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: userMessage },
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit reached. Please try again in a moment." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add funds." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("ai-expand error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
